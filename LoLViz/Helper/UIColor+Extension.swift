import UIKit

extension UIColor {
    @nonobjc class var background: UIColor { return UIColor(named: "background")! }
    @nonobjc class var darkGreen: UIColor { return UIColor(named: "darkGreen")! }
    @nonobjc class var neonGreen: UIColor { return UIColor(named: "neonGreen")! }
    @nonobjc class var pink: UIColor { return UIColor(named: "pink")! }
    @nonobjc class var blue: UIColor { return UIColor(named: "blue")! }
    @nonobjc class var purple: UIColor { return UIColor(named: "purple")! }
}
